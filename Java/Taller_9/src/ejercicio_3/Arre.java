    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField1.getText().equals("")) {
            if (esNumerico(jTextField1.getText())) {
                acceso.a単adir(Integer.parseInt(jTextField1.getText()));
                jTextField1.setText("");
                jTextField1.requestFocus();
                jButton5.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "No has ingresado un dato numerico.", "Vacio", JOptionPane.ERROR_MESSAGE);
                jTextField1.setText("");
                jTextField1.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(null, "No has ingresado nada en el campo de texto.", "Vacio", JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!acceso.estaVacio()) {
            JOptionPane.showMessageDialog(null, "Tu arreglo tiene datos, con " + acceso.getI() + " elemnto(s) hasta el momento.", "Lleno", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Tu arreglo no tiene datos, con 0 elemntos hasta el momento.", "Vacio", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (acceso.getI() > 0) {
            int a = (int) (acceso.primero());
            JOptionPane.showMessageDialog(null, "El primer numero de tu lista es : " + a + ".", "Dato 1", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Tu arreglo no tiene datos.", "Vacio", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (acceso.getI() > 0) {
            JOptionPane.showMessageDialog(null, "Se ha extraido el primer numero de tu lista '" + acceso.extraer() + "'.", "Extraido", JOptionPane.INFORMATION_MESSAGE);
            jButton5ActionPerformed(evt);
        } else {
            JOptionPane.showMessageDialog(null, "No hay datos que extraer.", "Vacio", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

